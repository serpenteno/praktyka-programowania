"""Funkcje matematyczne do testowania CI/CD"""


def add(a: int, b: int) -> int:
    """Zwraca sumę a + b."""
    return a + b


def subtract(a: int, b: int) -> int:
    """Zwraca różnicę a - b."""
    return a - b


def multiply(a: int, b: int) -> int:
    """Zwraca iloczyn a * b."""
    return a * b


def divide(a: int, b: int) -> float:
    """Zwraca iloraz a / b."""
    return a / b
